package at.qe.timeguess.dto;

public class TemporaryTestDTO {

	private String from;

	private String text;

	public TemporaryTestDTO(final String from, final String text) {
		super();
		this.from = from;
		this.text = text;
	}

	public String getFrom() {
		return from;
	}

	public String getText() {
		return text;
	}

}
